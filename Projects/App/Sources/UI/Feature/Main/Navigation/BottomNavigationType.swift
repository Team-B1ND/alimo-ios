import Foundation
import SwiftUI

enum BottomNavigationType: CaseIterable {
    case home
    case bookmark
    case my
    
    var text: String {
        switch self {
        case .home: "홈"
        case .bookmark: "북마크"
        case .my: "MY"
        }
    }
    
    var image: Image {
        switch self {
        case .home: Image(.home)
        case .bookmark: Image(.bookmarkTabbar)
        case .my: Image(.profile)
        }
    }
}
